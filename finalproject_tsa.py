def dm_test_from_errors(e1, e2):
    # squared-error DM for h=1
    from scipy import stats
    d = e1**2 - e2**2
    mean_d = d.mean()
    sd = d.std(ddof=1)
    T = len(d)
    denom = sd / math.sqrt(T) if sd>0 else np.nan
    dm = mean_d/denom if denom and denom>0 else np.nan
    p = 2*(1 - stats.norm.cdf(abs(dm))) if np.isfinite(dm) else np.nan
    return dm, p

