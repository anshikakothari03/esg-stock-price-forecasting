def ensure_company_features(df):
    """Take a DataFrame, create/normalize features in-place copy, and return it."""
    d = df.copy()
    # ensure string column names (fix tuple names if any)
    d.columns = [str(c) for c in d.columns]

    # ensure datetime index if possible
    try:
        d.index = pd.to_datetime(d.index)
    except Exception:
        # try common date columns
        date_col = find_col(d, ['date','update_date','timestamp','day'])
        if date_col:
            d.index = pd.to_datetime(d[date_col], errors='coerce')
    d = d.sort_index()

    # detect return / close / esg columns
    ret_col = find_col(d, ['return'])            # catches return, return_, returns...
    close_col = find_col(d, ['close','adj_close','price','last'])
    esg_col = find_col(d, ['esg','sustain','sustainalytics'])

    # If close exists, reindex to business days and forward-fill close so we can compute returns consistently
    if close_col:
        # reindex to business days from min->max and ffill close
        bidx = pd.date_range(d.index.min(), d.index.max(), freq=BDay())
        d = d.reindex(bidx)
        # coerce close to numeric then forward-fill
        d['close'] = pd.to_numeric(d[close_col], errors='coerce') if close_col != 'close' else pd.to_numeric(d['close'], errors='coerce')
        d['close'] = d['close'].ffill()
        # compute log_price and return freshly
        with np.errstate(divide='ignore', invalid='ignore'):
            d['log_price'] = np.where(d['close']>0, np.log(d['close']), np.nan)
        d['return'] = d['log_price'].diff()
    else:
        # no close: if return-like column exists, standardize name to 'return'
        if ret_col:
            d['return'] = pd.to_numeric(d[ret_col], errors='coerce')
        else:
            d['return'] = np.nan

    # Ensure ESG column exists (keep existing values if present)
    if esg_col:
        d['ESG'] = pd.to_numeric(d[esg_col], errors='coerce')
    else:
        # leave ESG as NaN (we won't invent values)
        d['ESG'] = d.get('ESG', np.nan)

    # fill ESG forward if it's monthly/quarterly (helps modeling); only forward-fill (no interpolation)
    if d['ESG'].notna().sum() > 0:
        d['ESG'] = d['ESG'].ffill().bfill()  # bfill as small safety in case first entries missing

    # engineered columns
    d['return_lag1'] = d['return'].shift(1)
    d['return_lag2'] = d['return'].shift(2)
    d['volatility_7d'] = d['return'].rolling(7, min_periods=1).std()
    d['volatility_30d'] = d['return'].rolling(30, min_periods=1).std()
    d['abs_return'] = d['return'].abs()

    # coerce numeric
    for col in ['log_price','return','return_lag1','return_lag2','volatility_7d','volatility_30d','abs_return','ESG']:
        if col in d.columns:
            d[col] = pd.to_numeric(d[col], errors='coerce')

    return d

