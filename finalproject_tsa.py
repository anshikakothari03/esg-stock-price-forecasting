def download_daily_prices(ticker, start="2018-01-01"):
    """
    Downloads daily price data for a given ticker.
    Returns a DataFrame with close and volume.
    """
    try:
        data = yf.download(ticker, start=start, progress=False)
        if data.empty:
            return pd.DataFrame()
        dfp = data[['Close', 'Volume']].rename(columns={'Close':'close','Volume':'volume'})
        dfp.index = pd.to_datetime(dfp.index)
        return dfp
    except Exception as e:
        print("Error downloading", ticker, ":", e)
        return pd.DataFrame()

