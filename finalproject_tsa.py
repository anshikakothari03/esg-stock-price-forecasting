    lstm_row = next((r for r in comp_metrics if r['Model']=='LSTM'), None)
    if classical and lstm_row:
        best_class = min(classical, key=lambda x: x['RMSE'])
        # load arrays for DM: align dates between best_class and LSTM
        best_df = models_for_comp[best_class['Model']]
        lstm_df_local = models_for_comp['LSTM']
        merged = best_df.merge(lstm_df_local[['date','y_pred']], on='date', suffixes=('_class','_lstm'))
        merged = merged.dropna(subset=['y_test','y_pred_class','y_pred_lstm'])
        if merged.shape[0] >= 10:
            e_class = merged['y_test'].values - merged['y_pred_class'].values
            e_lstm = merged['y_test'].values - merged['y_pred_lstm'].values
            dm_stat, pval = dm_test(e_class, e_lstm)
            dm_results.append({'Company': comp, 'BestClassical': best_class['Model'], 'LSTM': 'LSTM',
                               'DM_stat': dm_stat, 'p_value': pval, 'n': merged.shape[0]})
